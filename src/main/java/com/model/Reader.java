package com.model;

public class Reader {
    private int readerId;
    private int userId;
    private int money;

    public int getReaderId() {
        return readerId;
    }

    public int getUserId() {
        return userId;
    }

    public int getMoney() {
        return money;
    }

    public void setReaderId(int readerId) {
        this.readerId = readerId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public void setMoney(int money) {
        this.money = money;
    }
}
